package jbean;

public class Login
{
	long username;
	String password;

	public String getPassword()
	{
		return password;
	}

	public void setPassword(String password)
	{
		this.password = password;
	}

	public long getUsername()
	{
		return username;
	}

	public void setUsername(long username)
	{
		this.username = username;
	}
}
